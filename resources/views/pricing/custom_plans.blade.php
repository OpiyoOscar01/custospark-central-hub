<script>
  document.querySelectorAll('.toggleFeatures').forEach(button => {
    button.addEventListener('click', () => {
      const isExpanded = button.getAttribute('aria-expanded') === 'true';
      const featuresList = button.nextElementSibling;

      if (isExpanded) {
        featuresList.style.maxHeight = '0';
        button.textContent = "Show what's included";
        button.setAttribute('aria-expanded', 'false');
      } else {
        featuresList.style.maxHeight = featuresList.scrollHeight + 'px';
        button.textContent = "Hide what's included";
        button.setAttribute('aria-expanded', 'true');
      }
    });
  });

  // On load, collapse all feature lists
  window.addEventListener('load', () => {
    document.querySelectorAll('.featuresList').forEach(list => {
      list.style.maxHeight = '0';
    });
  });
</script>


